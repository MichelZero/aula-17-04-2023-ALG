# 6. Escreva um programa que verifica se um aluno foi aprovado ou reprovado em uma 
# disciplina com base em sua nota e imprime a mensagem correspondente.

nota = int(input("Digite a nota do aluno: "))
if nota >= 70:
    print("O aluno foi aprovado.")
else:
    print("O aluno foi reprovado.")
